-- Sample queries for analysis

-- 1. Top 10 most fragile states
SELECT rank, country, total, 
       (s1_demographic_pressures + s2_refugees_idps) as social_total,
       (c1_security_apparatus + c2_factionalized_elites + c3_group_grievance) as cohesion_total
FROM fsi_2023 
ORDER BY total DESC 
LIMIT 10;

-- 2. Top 10 most stable states
SELECT rank, country, total FROM fsi_2023 ORDER BY total ASC LIMIT 10;

-- 3. Average scores by category
SELECT 
    AVG(s1_demographic_pressures) as avg_demographic,
    AVG(s2_refugees_idps) as avg_refugees,
    AVG(c3_group_grievance) as avg_grievance,
    AVG(e1_economy) as avg_economy,
    AVG(p1_state_legitimacy) as avg_legitimacy,
    AVG(c1_security_apparatus) as avg_security
FROM fsi_2023;

-- 4. Countries with high security apparatus score (>8)
SELECT country, rank, c1_security_apparatus, total 
FROM fsi_2023 
WHERE c1_security_apparatus > 8 
ORDER BY c1_security_apparatus DESC;

-- 5. Create a view for easy reporting
CREATE VIEW fsi_summary AS
SELECT 
    country,
    rank,
    total,
    CASE 
        WHEN total >= 90 THEN 'Alert'
        WHEN total >= 60 THEN 'Warning'
        WHEN total >= 30 THEN 'Stable'
        ELSE 'Sustainable'
    END as status_category
FROM fsi_2023;
